#include <bits/stdc++.h>
using namespace std;
int main()
{

    // And operater
    cout << (5 & 7) << endl;
    // OR operater
    cout << (5 | 7) << endl;
    // XOR operater
    cout << (5 ^ 7) << endl;
    // not operater
    int a = 0;
    cout << ~a << endl;
    // left shift
     cout<<(5<<2)<<endl;
     //right shift
      cout<<(10>>2)<<endl;

    return 0;
}